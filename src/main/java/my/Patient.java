package my;

public class Patient {
    private String patient_ID, p_fio, p_address, p_district, p_policy, p_year, p_sign, p_department;

    public String getPatient_ID() {
        return patient_ID;
    }

    public void setPatient_ID(String patient_ID) {
        this.patient_ID = patient_ID;
    }

    public String getP_fio() {
        return p_fio;
    }

    public void setP_fio(String p_fio) {
        this.p_fio = p_fio;
    }

    public String getP_address() {
        return p_address;
    }

    public void setP_address(String p_address) {
        this.p_address = p_address;
    }

    public String getP_district() {
        return p_district;
    }

    public void setP_district(String p_district) {
        this.p_district = p_district;
    }

    public String getP_policy() {
        return p_policy;
    }

    public void setP_policy(String p_policy) {
        this.p_policy = p_policy;
    }

    public String getP_year() {
        return p_year;
    }

    public void setP_year(String p_year) {
        this.p_year = p_year;
    }

    public String getP_sign() {
        return p_sign;
    }

    public void setP_sign(String p_sign) {
        this.p_sign = p_sign;
    }

    public String getP_department() {
        return p_department;
    }

    public void setP_department(String p_department) {
        this.p_department = p_department;
    }
}
